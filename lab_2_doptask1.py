a = int(input('Введите первый коэффициент: '))
b = int(input('Введите второй коэффициент: '))
c = int(input('Введите третий коэффициент: '))

d = (b ** 2) - 4 * a * c

if d > 0:
  xf = (-b - (d ** 0.5)) / (2 * a)
  xs = (-b + (d ** 0.5)) / (2 * a)
  print(f'Корни уравнения: {xf}; {xs}')
elif d == 0:
  x = -b / (2 * a)
  print(f'Корень уравнения: {x}')
else:
  print('Корней нет')